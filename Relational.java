public class Relational {
    public static void main(String[] args) {
        float a=4.4f;
        float b=45.65f;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
    }
}
